        public static List<SecretSantaPair> DrawSecretSanta()
        {
            List<SecretSantaPair> result;
            do
            {
                result = TryDrawSecretSanta();
            } while (result == null);

            return result;
        }

        public static List<SecretSantaPair> RetryDrawSecretSanta()
        {
            List<SecretSantaPair> result;
            do
            {
                result = TryDrawSecretSantaAgain();
            } while (result == null);

            return result;
        }

        private static List<SecretSantaPair> TryDrawSecretSanta()
        {
            var familia = GetFamiliaCarvalho();

            var familiaParticipante = familia.Where(p => p.ParticipaAmigoOculto).ToList();
            var random = new Random();

            for (int i = 0; i < 3; i++)
            {
                familiaParticipante = familiaParticipante.OrderBy(p => random.Next()).ToList();
            }

            var available = new List<Parente>(familiaParticipante);
            var result = new List<SecretSantaPair>();

            foreach (var parente in familiaParticipante)
            {
                var possibleMatches = available.Where(p => p.Id != parente.Id && p.Casa != parente.Casa).ToList();
                if (possibleMatches.Count == 0)
                {
                    return null; // Return null to indicate failure
                }

                var match = possibleMatches[random.Next(possibleMatches.Count)];
                result.Add(new SecretSantaPair { Parente = parente, HiddenFriend = match });
                available.Remove(match);
            }

            return result;
        }

        private static List<SecretSantaPair> GetSecretSantaPairs()
        {
            var filePath = @"C:\Workspace\Familia\Familia\src\services\FML.Evento.API\Extensions\AmigoOculto2.txt";

            var secretSantaPairs = new List<SecretSantaPair>();

            if (!File.Exists(filePath))
            {
                throw new FileNotFoundException("The specified file was not found.", filePath);
            }

            var lines = File.ReadAllLines(filePath);
            SecretSantaPair currentPair = null;

            foreach (var line in lines)
            {
                if (line.StartsWith("Meu Nome:"))
                {
                    if (currentPair != null)
                    {
                        secretSantaPairs.Add(currentPair);
                    }
                    currentPair = new SecretSantaPair
                    {
                        Parente = new Parente
                        {
                            Nome = line.Substring("Meu Nome:".Length).Trim()
                        }
                    };
                }
                else if (line.StartsWith("Link Nome:"))
                {
                    if (currentPair != null)
                    {
                        currentPair.Parente.LinkNome = line.Substring("Link Nome:".Length).Trim();
                    }
                }
                else if (line.StartsWith("Meu Amigo Oculto:"))
                {
                    if (currentPair != null)
                    {
                        currentPair.HiddenFriend = new Parente
                        {
                            Nome = line.Substring("Meu Amigo Oculto:".Length).Trim()
                        };
                    }
                }
                else if (line.StartsWith("Telefone:"))
                {
                    if (currentPair != null)
                    {
                        currentPair.Parente.Telefone = line.Substring("Telefone:".Length).Trim();
                    }
                }

            }

            if (currentPair != null)
            {
                secretSantaPairs.Add(currentPair);
            }


            return secretSantaPairs;
        }

        private static List<SecretSantaPair> TryDrawSecretSantaAgain()
        {
            var secretSantaPairs = GetSecretSantaPairs();
            var familia = GetFamiliaCarvalho();

            var amigoOculto = familia.Where(p => p.ParticipaAmigoOculto).ToList();

            var dcAmigoOculto = amigoOculto.GroupBy(p => p.Nome).ToDictionary(p => p.Key, p => p.First());

            foreach (var pairs in secretSantaPairs)
            {
                if(dcAmigoOculto.ContainsKey(pairs.Parente.Nome))
                {
                   var parente = dcAmigoOculto[pairs.Parente.Nome];
                    pairs.Parente = parente;
                }

                if (dcAmigoOculto.ContainsKey(pairs.HiddenFriend.Nome))
                {
                    var parente = dcAmigoOculto[pairs.HiddenFriend.Nome];
                    pairs.HiddenFriend = parente;
                }
            }

            return secretSantaPairs;

        }

